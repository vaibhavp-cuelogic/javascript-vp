/*
	Write a program that can make the use of 'reserved' keywords in JS without getting any error.
*/

	var obj = {};
	
	obj.try= "try is reserved keyword in javascript";
	obj.typeof= "typeof is reserved keyword in javascript";
	obj.var= "var is reserved keyword in javascript";
	obj.void= "void is reserved keyword in javascript";
	obj.private= "private is reserved keyword in javascript";


	console.log('Value Of try keyword is:'+obj.try);
	console.log('Value Of typeof keyword is:'+obj.typeof);
	console.log('Value Of var keyword is:'+obj.var);
	console.log('Value Of void keyword is:'+obj.void);
	console.log('Value Of private keyword is:'+obj.private);
